package com.holonplatform.vaadin.flow.navigator.test.data;

public class TestParameterType {

	private final int value;

	public TestParameterType(int value) {
		super();
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + value;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TestParameterType other = (TestParameterType) obj;
		if (value != other.value)
			return false;
		return true;
	}

}
